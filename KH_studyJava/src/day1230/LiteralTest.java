package day1230;

public class LiteralTest {
    public static void main(String[] args) {
        long num1 = 100000000L;
        float num2 = 3.14f;

        int num3 = 017; // 8진수10 숫자앞에 0
        System.out.println(num3);

        int num4 = 0x10;
        System.out.println(num4);

        num4 =  0xf;
        System.out.println(num4);
    }
}
